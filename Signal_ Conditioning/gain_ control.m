# Gain Control

The LM358 preamp gain stage was adjusted to amplify the microphone signal without excessive distortion.

## Purpose

The microphone signal required amplification before analog sampling and FFT processing.

## Main Goals

- Increase signal amplitude
- Prevent clipping
- Maintain waveform stability
- Improve frequency detection accuracy

## Tuning Process

Gain was adjusted experimentally while monitoring:

- Signal amplitude
- FFT stability
- Display behavior
- Audio classification consistency

## Notes

Proper gain tuning was important because excessive amplification caused unstable waveform behavior and inaccurate frequency classification.
